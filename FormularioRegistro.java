    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // Que radioButton (opcion) fue seleccionada
        if (jRadioButton1.isSelected()) {
            JOptionPane.showMessageDialog(this, "La opcion 1 ha sido seleccionada");
        }
        if (jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(this, "La opcion 2 ha sido seleccionada");
        }
    }//GEN-LAST:event_jButton3MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // Que CheckBox (Casilla) fue seleccionada
        if (jCheckBox1.isSelected() && jCheckBox2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Ambas casillas han sido seleccionadas");
        } else if (jCheckBox1.isSelected()) {
            JOptionPane.showMessageDialog(this, "La casilla 1 ha sido seleccionada");
        } else if (jCheckBox2.isSelected()) {
            JOptionPane.showMessageDialog(this, "La casilla 2 ha sido seleccionada");
        } else{
            JOptionPane.showMessageDialog(this, "Ninguna casilla ha sido seleccionada");
        }
        
    }//GEN-LAST:event_jButton4MouseClicked

