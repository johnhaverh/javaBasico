    // Metodo borra el JList y devuelve un modelo vacio
    public DefaultListModel limpiarList() {
        DefaultListModel modelo = new DefaultListModel();
        jList1.setModel(modelo);
        return modelo;
    }
    
    // Metodo agregar un elemento a la lista y devuelve el modelo
    public DefaultListModel agregar() {
        DefaultListModel modelo =  (DefaultListModel) jList1.getModel();
        modelo.addElement(jTextField1.getText());
        return modelo;
    }
    
    // Metodo que elimina un elemento de la lista y devuelve el modelo
    public DefaultListModel eliminar() {
        DefaultListModel modelo = (DefaultListModel) jList1.getModel();
        modelo.remove(jList1.getSelectedIndex());
        return modelo;
    }

